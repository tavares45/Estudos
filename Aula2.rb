# maneiras de concatenar: aspas duplas permitem interpolação usando a hashtag
v1 = 'tavares'

# Concatenação de string usando o operador de adição
puts "Hello!" + v1

v2 = "matheus tavares"

# Interpolação de variável em uma string usando aspas duplas e hashtag
puts "Beleza? #{v2}" 

# Interpolação de expressão em uma string usando aspas duplas e hashtag
puts "A soma de 1 + 2 é: #{1 + 2}"
